# Global environment configuration and constants for tea-break.


# Prefix for the installed files. For example, if set to /usr/local, then
# commands will be installed in /usr/local/bin etc.
PREFIX="/usr/local"

# Directory to store files created at runtime, such as the enabled file,
# and the old user crontab. By default this is under the user's home
# directory, because if the old crontab were stored under /tmp, it would
# be lost when the computer is powered down.
RUNTIME="~/.tea-break"

